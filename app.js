let currentQuestion = 0;
const questionContainer = document.getElementById("question-container");
const answerContainer = document.getElementById("answer-container");
const prevBtn = document.getElementById("prev-btn");
const nextBtn = document.getElementById("next-btn");
const resultContainer = document.getElementById("result-container");
let userAnswers = [];

function showQuestion() {
    const question = questions[currentQuestion];
    questionContainer.innerHTML = `<div class="question">${question.question}</div>`;
    answerContainer.innerHTML = `
        ${question.options.map(option => `
            <div>
                <label>
                    <input type="radio" name="answer" value="${option.value}"> ${option.text}
                </label>
            </div>
        `).join('')}
    `;
    prevBtn.disabled = currentQuestion === 0;
    nextBtn.disabled = false;
}

function showResult(type) {
    resultContainer.innerHTML = `<h2>Your Enneagram Type: ${type}</h2><p>${typeDescriptions[type]}</p>`;
    resultContainer.style.display = "block";
    prevBtn.disabled = true;
    nextBtn.disabled = true;
}

prevBtn.addEventListener("click", () => {
    currentQuestion--;
    showQuestion();
});

nextBtn.addEventListener("click", () => {
    const selectedAnswer = document.querySelector('input[name="answer"]:checked');
    if (selectedAnswer) {
        userAnswers.push(parseInt(selectedAnswer.value));
        currentQuestion++;
        if (currentQuestion < questions.length) {
            showQuestion();
        } else {
            const result = calculateResult();
            showResult(result);
        }
    } else {
        alert("කරුණාකර පිළිතුරක් තෝරන්න.");
    }
});

showQuestion();

function calculateResult() {
    const typeScores = {};
    questions.forEach((question, index) => {
        const type = question.type;
        const answer = userAnswers[index];
        typeScores[type] = (typeScores[type] || 0) + answer;
    });

    let maxScore = 0;
    let result = null;
    for (const type in typeScores) {
        if (typeScores[type] > maxScore) {
            maxScore = typeScores[type];
            result = type;
        }
    }

    return result;
}
